// |   UPPER AND LOWER CASE METHODS   |
// |==================================|

public class Main_2_6_3 {

	public static void main(String[] args) {
		
		// Upper and lower case methods convert strings into either upper case or lower case
		
		String lowercase = "abcdefg";
		
		String uppercase = "HIJKLMNOP";
		
		System.out.println(lowercase.toUpperCase()); // Output: ABCDEFG
		System.out.println(uppercase.toLowerCase()); // Output: hijklmnop
		
	}
}
